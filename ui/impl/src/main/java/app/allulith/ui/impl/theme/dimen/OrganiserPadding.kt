package app.allulith.ui.impl.theme.dimen

import androidx.compose.ui.unit.Dp

/**
 * Works on a percentage scale with the default measurement being 8.dp therefore,
 * pad200 is 200% of 8.dp equaling 16.dp this can be applied to all other measurements
 */
data class OrganiserPadding(
    val pad050: Dp,
    val pad100: Dp,
    val pad150: Dp,
    val pad200: Dp,
    val pad250: Dp,
    val pad300: Dp,
    val pad350: Dp,
    val pad400: Dp,
)
